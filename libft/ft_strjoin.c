#include "libft.h"

// ft_strjoin combines two strings into a new string,
// allocating memory for the resulting string dynamically.
// It is important to note that the caller of this function
// is responsible for freeing the memory allocated
// for the resulting string using 'free' when
// it is no longer needed to avoid memory leaks.

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*result;
	int		i;
	int		j;

	i = 0;
	j = 0;
	if (!s1 || !s2)
		return (NULL);
	result = (char *)malloc(sizeof(char) * (ft_strlen(s1) + ft_strlen(s2) + 1));
	if (!result)
		return (NULL);
	while (s1[i])
	{
		result[i] = s1[i];
		i++;
	}
	while (s2[j])
	{
		result[i] = s2[j];
		i++;
		j++;
	}
	result[i] = '\0';
	return (result);
}
